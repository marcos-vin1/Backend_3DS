export function racaGato(racaGato, TipoBanho) {
return `Gato ${racaGato} comprou raça tipo: ${TipoBanho}`   
}


export function tosaGato(racaGato, tipoTosa){
return `Gato ${racaGato} deu banho: ${tipoTosa}`
}
export function alimentaGato(racaGato, tiRacao) {
return `Gato ${racaGato}comprou ração tipo: ${tiRacao}`    
}








